// > <
console.log("3 > 4", 3 > 4);
console.log("3 < 4", 3 < 4);
// >=, <=
console.log("3 >= 4", 3 >= 4);
console.log("3 <= 4", 3 <= 4);
console.log("3 >= 3", 3 >= 3);
console.log("3 <= 3", 3 <= 3);
// ==
console.log("3 == 3", 3 == 3);
console.log('3 == "3"', 3 == "3");
console.log("3 == 4", 3 == 4);
console.log('3 == "4"', 3 == "4");
// ===
console.log("3 === 3", 3 === 3);
console.log('3 === "3"', 3 === "3");
console.log("3 === 4", 3 === 4);
console.log('3 === "4', 3 === "4");
// !=
console.log("3 != 3", 3 != 3);
console.log('3 != "3"', 3 != "3");
console.log("3 != 4", 3 != 4);
console.log('3 != "4"', 3 != "4");
// !==
console.log("3 !== 3", 3 !== 3);
console.log('3 !== "3"', 3 !== "3");
console.log("3 !== 4", 3 !== 4);
console.log('3 !== "4"', 3 !== "4");
// &&
console.log("true && true", true && true);
console.log("true && false", true && false);
console.log("false && true", false && true);
console.log("false && false", false && false);
// ||
console.log("true || true", true || true);
console.log("true || false", true || false);
console.log("false || true", false || true);
console.log("false || false", false || false);
// !
console.log("!true", !true);
console.log("!false", !false);

